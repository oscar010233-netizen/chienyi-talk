function section(title) {
  return [
    "",
    "-- =====================================================================",
    `-- ${title}`,
    "-- =====================================================================",
    "",
  ].join("\n");
}

async function buildBundle(bundle) {
  const parts = [
    "-- Generated by scripts/build-supabase-sql-bundles.mjs.",
    "-- Review before running in Supabase SQL Editor.",
    `-- Bundle: ${bundle.name}`,
    `-- Purpose: ${bundle.purpose}`,
    "",
  ];

  for (const relativeFile of bundle.files) {
    const absoluteFile = path.join(root, relativeFile);
    const sql = await readFile(absoluteFile, "utf8");
    parts.push(section(relativeFile));
    parts.push(sql.trimEnd());
    parts.push("");
  }

  await writeFile(path.join(outDir, bundle.name), `${parts.join("\n")}\n`, "utf8");
}

await mkdir(outDir, { recursive: true });

for (const bundle of bundles) {
  await buildBundle(bundle);
  console.log(`wrote supabase/bundles/${bundle.name}`);
}
